    private void cargar_tabla() {
        DefaultTableModel tab = (DefaultTableModel) tbl_tabla_usuario.getModel();
        tab.setRowCount(0);
        try {
            Conexion.getInstanciaConexion().listar_datos("select * from usuarios ");
            while (Conexion.getInstanciaConexion().rt.next()) {
                tab.addRow(new Object[]{
                    Conexion.getInstanciaConexion().rt.getString("idusuario"),
                    Conexion.getInstanciaConexion().rt.getString("usuario"),
                    Conexion.getInstanciaConexion().rt.getString("contraseña")});

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar tabla" + e);
        }
    }

    private void deshacertransaccion() {
        try {
            Conexion.getInstanciaConexion().obtenerRutaConeccion().rollback();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar tabla" + e);
        }
    }

    private void recuperar_usuario(String vcod) {
        try {
            st = Conexion.getInstanciaConexion().obtenerRutaConeccion().createStatement();
            rs = st.executeQuery("SELECT idusuario, usuario, contraseña\n"
                    + "	FROM usuarios"
                    + " where idusuario=" + vcod);
            if (rs.next()) {
                txt_codigo_usuario.setText(rs.getString("idusuario"));
                txt_usuario.setText(rs.getString("usuario"));
                txt_contraseña.setText(rs.getString("contraseña"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al recuperar usuario" + e);
        }
    }
