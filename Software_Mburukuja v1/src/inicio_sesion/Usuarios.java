    private void bt_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_guardarActionPerformed
        if (txt_usuario.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar la ciudad", "Atencion!!", JOptionPane.WARNING_MESSAGE);
            txt_usuario.requestFocus();
            return;
        }
        if (txt_contraseña.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar la ciudad", "Atencion!!", JOptionPane.WARNING_MESSAGE);
            txt_contraseña.requestFocus();
            return;
        }
        int sms = JOptionPane.showConfirmDialog(null, "¿Desea grabar", "Grabar", JOptionPane.YES_NO_OPTION);
        if (sms == 0) {
            if (ban == 1) {
                try {
                    if (Conexion.
                            getInstanciaConexion().
                            obtenerRutaConeccion().
                            getAutoCommit()) {

                        Conexion.getInstanciaConexion().
                                obtenerRutaConeccion().
                                setAutoCommit(false);
                    }
                    PreparedStatement p = Conexion.getInstanciaConexion().
                            obtenerRutaConeccion().
                            prepareStatement("INSERT INTO usuarios(\n"
                                    + "	idusuario,usuario,contraseña)\n"
                                    + "	VALUES (?, ?, ?);");
                    p.setInt(1, Integer.parseInt(txt_codigo_usuario.getText()));
                    p.setString(2, (txt_usuario.getText()));
                    p.setString(3, (txt_contraseña.getText()));
                    p.executeUpdate();
                    Conexion.getInstanciaConexion().obtenerRutaConeccion().commit();
                    cargar_tabla();
                    bt_cancelar.doClick();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error al grabar nuevo registro" + e);
                    deshacertransaccion();
                }
            }
        }
    }//GEN-LAST:event_bt_guardarActionPerformed

    private void bt_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_eliminarActionPerformed
        if (txt_codigo_usuario.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
            return;
        }
        int sms = JOptionPane.showConfirmDialog(null, "¿Desea eliminar el registro seleccionar?",
                "Atencion!!!", JOptionPane.YES_NO_OPTION);
        if (sms == 0) {
            try {
                if (Conexion.
                        getInstanciaConexion().
                        obtenerRutaConeccion().
                        getAutoCommit()) {
                    Conexion.getInstanciaConexion().obtenerRutaConeccion().setAutoCommit(false);
                }

                PreparedStatement pe = Conexion.getInstanciaConexion().
                        obtenerRutaConeccion().
                        prepareStatement("delete from usuarios "
                                + " where idusuario=?");
                pe.setInt(1, Integer.parseInt(txt_codigo_usuario.getText()));
                pe.execute();
                Conexion.getInstanciaConexion().obtenerRutaConeccion().commit();
                cargar_tabla();
                bt_cancelar.doClick();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un registro" + e);
            }
        }
    }//GEN-LAST:event_bt_eliminarActionPerformed

